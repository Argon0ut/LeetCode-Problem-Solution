from typing import List


class Solution:
    def Sum(self, numbers: List[int], target: int) -> List[int]:
        left = 0
        right = len(numbers) - 1
        while left < right:
            if numbers[left] + numbers[right] == target:
                return [left + 1, right + 1]
            elif numbers[left] + numbers[right] < target:
                left += 1
            else:
                right -= 1

numbers = [0, 0]
target = 0
ans = Solution().Sum(numbers, target)
print(ans)